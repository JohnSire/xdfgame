package com.huass.weixin.utils;


public class SchoolBagBingo { 
	
	private String id;
	private String date;
	private String headImage;
	private String numN;
	private String leavess;
	
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHeadImage() {
		return headImage;
	}
	public void setHeadImage(String headImage) {
		this.headImage = headImage;
	}
	public String getNumN() {
		return numN;
	}
	public void setNumN(String numN) {
		this.numN = numN;
	}
	public String getLeavess() {
		return leavess;
	}
	public void setLeavess(String leavess) {
		this.leavess = leavess;
	}
	
}
